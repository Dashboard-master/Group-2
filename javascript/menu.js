function closePopup() {
  let box = document.getElementById("popup");
  box.style = "display: none"
}

function getItems(category) {
  let items;
  switch (category) {
    case "coldbrew":
      items = coldBrews
      break
    case "warmbrew":
      items = warmBrews
      break
    case "dishes":
      items = dishes
      break
    case "desserts":
      items = desserts
      break
  }
  console.log(category)
  return items
}

function showPopup(num, category) {
  let box = document.getElementById("popup");
  let items = getItems(category)
  let item = items[num]
  box.innerHTML = `<div class="content">
              <span class="close" onclick="closePopup()">&times;</span>
              <h1 class="click-h1">${item.title}</h1>
               <p class="text">${item.text}
              <img id="content-pic" src="${item.img.src}" ${item.img.alt}>
              <p class="popup_price">${(item.price)}</p>
              </p>
              </div>`
  box.style = "display:block"
}

function addItems(category, parentId) {
  let parent = document.getElementById(parentId);
  let items = getItems(category)
  console.log(items)
  for (let i = 0; i < items.length; i++) {
    let item = items[i]
    parent.innerHTML += `<ul>
    <li class="name" onclick="showPopup(${i},'${category}')">${item.title}</li>
    <li>${item.price}</li>
</ul>`
  }
}
